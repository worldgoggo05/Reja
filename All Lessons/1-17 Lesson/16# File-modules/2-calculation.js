// calculation.js:
let calculation = {};

calculation.multiple = (a, b) => {
	return a * b;
};

calculation.divide = (a, b) => {
	return a / b;
};

calculation.plus = (a, b) => {
	return a + b;
};

calculation.minus = (a, b) => {
	return a - b;
};

module.exports = calculation;
